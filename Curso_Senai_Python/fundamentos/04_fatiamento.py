""""
Exemplo de fatimento de string em python
Obs: sempre preciso colocar uma posição mais
"""

cidade = "Piracicaba"
primeiras_4_letras = cidade[0:4]

print(f"As quatro primeiras letras da cidade {cidade} são {primeiras_4_letras}")

outras_letras = cidade[2:6]
print(f"Outras letras {outras_letras}")

duas_ultimas_letras = cidade[-2:]
print(f"Duas últimas letras {duas_ultimas_letras}")